# Task 2: Sum of Integers from 1 to 50 Using a Loop

# Step 1: Initialize a variable to store the sum
total_sum = 0

# Step 2: Use a for loop to iterate over numbers from 1 to 50
for i in range(1, 51):   # range(1, 51) includes numbers from 1 to 50
    total_sum += i       # Add each number to total_sum

# Step 3: Display the final sum
print(f"The sum of integers from 1 to 50 is: {total_sum}")
